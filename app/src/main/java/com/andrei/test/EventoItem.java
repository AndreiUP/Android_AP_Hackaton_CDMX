package com.andrei.test;

public class EventoItem {
    String titulo;
    String contenido;
    String imagePath;
    String procentajeRecomendacion;

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getContenido() {
        return contenido;
    }

    public void setContenido(String contenido) {
        this.contenido = contenido;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }

    public String getProcentajeRecomendacion() {
        return procentajeRecomendacion;
    }

    public void setProcentajeRecomendacion(String procentajeRecomendacion) {
        this.procentajeRecomendacion = procentajeRecomendacion;
    }
}
